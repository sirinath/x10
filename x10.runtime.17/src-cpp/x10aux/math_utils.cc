#include <x10aux/math_utils.h>

#include <math.h>

x10_double x10aux::math_utils::pow_mu(x10_double x, x10_double y) {
    return pow(x,y);
}

x10_double x10aux::math_utils::exp_mu(x10_double x) {
    return exp(x);
}

x10_double x10aux::math_utils::cos_mu(x10_double x) {
    return cos(x);
}

x10_double x10aux::math_utils::sin_mu(x10_double x) {
    return sin(x);
}

x10_double x10aux::math_utils::sqrt_mu(x10_double x) {
    return sqrt(x);
}
