/** Interface supported by all X10 classes that support array style access.
 *  A class supporting this interface must supply 
 * @author vj Jan 19, 2005
 * 
 */
public interface IndexableGet/*<T>*/ {
	
	// /*T*/Object get(int i);
	// /*T*/Object get(int i, int j);
	// /*T*/Object get(int i, int j, int k);
	// /*T*/Object get(int i, int j, int k, int l);
	// /*T*/Object get(int i, int j, int k, int l, int m);
	// /*T*/Object get(int[] p);

}
